export class Unite {
    id: number;
    firstName: string;
    lastName: string;
    email: string;
    age: number;
    isActive: boolean;
  
    constructor(id: number, firstName: string, lastName: string, email: string, age: number, isActive: boolean) {
      this.id = id;
      this.firstName = firstName;
      this.lastName = lastName;
      this.email = email;
      this.age = age;
      this.isActive = isActive;
    }
  }
  